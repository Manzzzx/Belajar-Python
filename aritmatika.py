angka_1 = 10
angka_2 = 2

# Penjumlahan ( + )
penjumlahan = angka_1 + angka_2         #hasil : 12
print(penjumlahan)

# Pengurangan ( - )
pengurangan = angka_1 - angka_2         #hasil : 8
print(pengurangan)

# Pembagian ( / )
pembagian = angka_1 / angka_2           #hasil : 5.0
print(pembagian)

# Perkalian ( * )
perkalian = angka_1 * angka_2           #hasil :  20
print(perkalian)

# Pembagian tanpa koma ( // )
pembagian_bulat = angka_1 // angka_2    #hasil : 5
print(pembagian_bulat)

# Pemangkatan ( ** )
pangkat = angka_1**angka_2              #hasil : 100
print(pangkat)